-- ==================== INDEXES ====================
CREATE INDEX IF NOT EXISTS idx_prediction_events_ticket_id
    ON incident_prediction_events(ticket_id);

CREATE INDEX IF NOT EXISTS idx_prediction_events_risk_level
    ON incident_prediction_events(predicted_risk_level);

CREATE INDEX IF NOT EXISTS idx_training_feedback_ticket_id
    ON incident_training_feedback(ticket_id);

CREATE INDEX IF NOT EXISTS idx_training_feedback_issue_type
    ON incident_training_feedback(issue_type);

CREATE INDEX IF NOT EXISTS idx_training_feedback_final_risk
    ON incident_training_feedback(final_risk_level);
